import type { TechnicalSnapshot, PricePoint } from "@/types/technical";

function generatePriceSeries(start: number, days = 90, volatility = 0.018, drift = 0.0015): PricePoint[] {
  const points: PricePoint[] = [];
  let price = start;
  const today = new Date();
  for (let i = days - 1; i >= 0; i--) {
    const d = new Date(today);
    d.setDate(today.getDate() - i);
    const shock = (Math.sin(i * 0.4) + Math.cos(i * 0.13)) * volatility;
    price = +(price * (1 + drift + shock)).toFixed(2);
    const sma50 = +(price * (0.98 + Math.sin(i * 0.05) * 0.01)).toFixed(2);
    const sma200 = +(price * (0.93 + Math.sin(i * 0.02) * 0.005)).toFixed(2);
    const ema20 = +(price * (0.99 + Math.sin(i * 0.08) * 0.008)).toFixed(2);
    const vwap = +(price * 0.995).toFixed(2);
    const upperBB = +(price * 1.04).toFixed(2);
    const lowerBB = +(price * 0.96).toFixed(2);
    points.push({
      date: d.toISOString().slice(0, 10),
      close: price,
      volume: Math.round(1_000_000 + Math.abs(Math.sin(i * 0.3)) * 500_000),
      sma50,
      sma200,
      ema20,
      vwap,
      upperBB,
      lowerBB,
    });
  }
  return points;
}

