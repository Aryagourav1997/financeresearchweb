export function findCompany(query: string): Company | undefined {
  const q = query.trim().toLowerCase();
  if (!q) return undefined;
  return (
    mockCompanies.find((c) => c.id === q) ||
    mockCompanies.find((c) => c.ticker.toLowerCase() === q) ||
    mockCompanies.find((c) => c.isin.toLowerCase() === q) ||
    mockCompanies.find((c) => c.name.toLowerCase().includes(q)) ||
    mockCompanies.find((c) => c.industry.toLowerCase().includes(q)) ||
    mockCompanies.find((c) => c.sector.toLowerCase().includes(q))
  );
}
